fn session_disabled_reason(
    has_live_session: bool,
    detached_readonly: bool,
    provider_name: &str,
) -> Option<String> {
    if detached_readonly {
        Some("Detached windows are read only".to_string())
    } else if !has_live_session {
        Some(format!("Start a live {provider_name} session first"))
    } else {
        None
    }
}

