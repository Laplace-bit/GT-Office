#[derive(Debug, Clone, Copy, Serialize, Deserialize, PartialEq, Eq)]
#[serde(rename_all = "snake_case")]
pub enum ToolCommandProviderKind {
    Claude,
    Codex,
    Gemini,
    Shell,
    Unknown,
    Any,
}

impl From<Option<AgentToolKind>> for ToolCommandProviderKind {
    fn from(value: Option<AgentToolKind>) -> Self {
        match value {
            Some(AgentToolKind::Claude) => Self::Claude,
            Some(AgentToolKind::Codex) => Self::Codex,
            Some(AgentToolKind::Gemini) => Self::Gemini,
            Some(AgentToolKind::Shell) => Self::Shell,
            Some(AgentToolKind::Unknown) | None => Self::Any,
        }
    }
}

