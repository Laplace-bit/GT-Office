#[derive(Debug, Clone, Serialize, Deserialize, PartialEq, Eq)]
#[serde(rename_all = "camelCase")]
pub struct ToolCommandCatalogItem {
    pub id: String,
    pub label: String,
    #[serde(default, skip_serializing_if = "Option::is_none")]
    pub short_label: Option<String>,
    #[serde(default, skip_serializing_if = "Option::is_none")]
    pub slash_command: Option<String>,
    pub command_family: ToolCommandFamily,
    #[serde(default, skip_serializing_if = "Option::is_none")]
    pub tooltip: Option<String>,
    pub icon: String,
    pub provider_kind: ToolCommandProviderKind,
    pub kind: ToolCommandKind,
    pub category: ToolCommandCategory,
    pub surface_target: ToolCommandSurfaceTarget,
    pub scope_kind: ToolCommandScopeKind,
    pub group: String,
    pub priority: i32,
    pub presentation: ToolCommandPresentation,
    pub danger_level: ToolCommandDangerLevel,
    pub default_pinned: bool,
    pub enabled: bool,
    #[serde(default, skip_serializing_if = "Option::is_none")]
    pub disabled_reason: Option<String>,
    pub requires_live_session: bool,
    pub supports_detached_window: bool,
    pub supports_parallel_targets: bool,
    pub execution: ToolCommandExecution,
    #[serde(default, skip_serializing_if = "Vec::is_empty")]
    pub arguments: Vec<ToolCommandArgument>,
}

fn text_command(
    id: &str,
    label: &str,
    short_label: Option<&str>,
    slash_command: Option<&str>,
    command_family: ToolCommandFamily,
    tooltip: Option<&str>,
    icon: &str,
    provider_kind: ToolCommandProviderKind,
    group: &str,
    priority: i32,
    category: ToolCommandCategory,
    command_text: &str,
    submit: bool,
    danger_level: ToolCommandDangerLevel,
    kind: ToolCommandKind,
    default_pinned: bool,
    enabled: bool,
    disabled_reason: Option<String>,
    requires_live_session: bool,
    supports_parallel_targets: bool,
) -> ToolCommandCatalogItem {
    ToolCommandCatalogItem {
        id: id.to_string(),
        label: label.to_string(),
        short_label: short_label.map(str::to_string),
        slash_command: slash_command.map(str::to_string),
        command_family,
        tooltip: tooltip.map(str::to_string),
        icon: icon.to_string(),
        provider_kind,
        kind,
        category,
        surface_target: ToolCommandSurfaceTarget::Terminal,
        scope_kind: ToolCommandScopeKind::Station,
        group: group.to_string(),
        priority,
        presentation: ToolCommandPresentation::Direct,
        danger_level,
        default_pinned,
        enabled,
        disabled_reason,
        requires_live_session,
        supports_detached_window: true,
        supports_parallel_targets,
        execution: ToolCommandExecution::InsertText {
            text: command_text.to_string(),
            submit,
        },
        arguments: Vec::new(),
    }
}

